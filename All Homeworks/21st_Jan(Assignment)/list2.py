# Given a list, remove the last element and print the updated list.

dashavatara = ["Matsya", "Kurma", "Varaha", "Narasimha", "Vamana", "Parashurama", "Rama", "Krishna", "Buddha", "Kalki"]
print(f"\nTotal list : {dashavatara}")
dashavatara.pop()
print(f"Total list after removing the last element : {dashavatara}\n")

